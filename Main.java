public class Main {

    // 주석
    // 자바 프로그램 시작과 종료 지점
    public static void main(String[] args) {
        // 출력
        System.out.println(100);
        System.out.println(200);

        // 프로그램은 위에서 아래로 순차적으로 실행됨

        // 문자 출력
        System.out.println("안녕");
        System.out.println("잘가");

        // 아무거나 10개 출력
        System.out.println("*");
        System.out.println("**");
        System.out.println(26);
        System.out.println("****");
        System.out.println("*****");
        System.out.println("******");
        System.out.println("*******");
        System.out.println("********");
        System.out.println("*********");
        System.out.println("**********");

    }
}
