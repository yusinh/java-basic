package day3;

public class Calculator {
    public static int plus(int num1, int num2) {
        return num1 + num2;
    }
    public static int minus(int num1, int num2) {
        return num1 - num2;
    }
    public static int multiply(int num1, int num2) {
        return num1 * num2;
    }
    public static int divide(int num1, int num2) {
        return num1 / num2;
    }
}
